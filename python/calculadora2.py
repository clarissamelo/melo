x = int (input("digite o primeiro número: "))
y = int (input("digite o segundo número: "))

resultadoSOM = x + y
resultadoSUB = x - y
resultadoMUL = x * y
resultadoDIV = x / y 
#realizar o calculo
print(f" {x} + {y} = {resultadoSOM}")
print(f" {x} - {y} = {resultadoSUB}")
print(f" {x} * {y} = {resultadoMUL}")
print(f" {x} / {y} = {resultadoDIV}")
